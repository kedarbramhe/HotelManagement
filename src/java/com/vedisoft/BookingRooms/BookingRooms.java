package com.vedisoft.BookingRooms;

/**
 *
 * @author vedisoft
 */
public class BookingRooms {

    int id;
    int bookingId;
    int roomNo;

    public BookingRooms() {
    }

    public BookingRooms(int id, int bookingId, int roomNo) {
        this.id = id;
        this.bookingId = bookingId;
        this.roomNo = roomNo;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getBookingId() {
        return bookingId;
    }

    public void setBookingId(int bookingId) {
        this.bookingId = bookingId;
    }

    public int getRoomNo() {
        return roomNo;
    }

    public void setRoomNo(int roomNo) {
        this.roomNo = roomNo;
    }
}
