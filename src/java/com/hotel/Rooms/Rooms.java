package com.vedisoft.Rooms;
/**
 *
 * @author vedisoft
 */
public class Rooms {

    int roomNo;
    int floorNo;
    int rcategoryId;

    public Rooms() {
    }

    public Rooms(int roomNo, int floorNo, int rcategoryId) {
        this.roomNo = roomNo;
        this.floorNo = floorNo;
        this.rcategoryId = rcategoryId;
    }

    public int getRoomNo() {
        return roomNo;
    }

    public void setRoomNo(int roomNo) {
        this.roomNo = roomNo;
    }

    public int getFloorNo() {
        return floorNo;
    }

    public void setFloorNo(int floorNo) {
        this.floorNo = floorNo;
    }

    public int getRcategoryId() {
        return rcategoryId;
    }

    public void setRcategoryId(int rcategoryId) {
        this.rcategoryId = rcategoryId;
    }
}
