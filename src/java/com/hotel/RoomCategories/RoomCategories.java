package com.vedisoft.RoomCategories;
/**
 *
 * @author vedisoft
 */
public class RoomCategories {

    int rcategoryId;
    String rcategoryName;
    String rcategoryDetails;

    public RoomCategories() {
    }

    public RoomCategories(int rcategoryId, String rcategoryName, String rcategoryDetails) {
        this.rcategoryId = rcategoryId;
        this.rcategoryName = rcategoryName;
        this.rcategoryDetails = rcategoryDetails;
    }

    public int getRcategoryId() {
        return rcategoryId;
    }

    public void setRcategoryId(int rcategoryId) {
        this.rcategoryId = rcategoryId;
    }

    public String getRcategoryName() {
        return rcategoryName;
    }

    public void setRcategoryName(String rcategoryName) {
        this.rcategoryName = rcategoryName;
    }

    public String getRcategoryDetails() {
        return rcategoryDetails;
    }

    public void setRcategoryDetails(String rcategoryDetails) {
        this.rcategoryDetails = rcategoryDetails;
    }
}
